package oops;

public class Assignment_Arithmetic_operators 
{
	
	 public static void main(String[] args)
	 {
	 
	 int a = 56;
	 float b = 98.89f;
	 int c = 789;
	 
	 System.out.println(" a+b+c: " + (a+b+c));
	 System.out.println(" a-b-c: " + (a-b-c));
	 System.out.println(" a*b*c: " + (a*b*c));
	 System.out.println("b/2 :"+ (b/2));
	 
	 
	 
	 }

}
